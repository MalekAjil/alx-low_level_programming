/**
 * add - add two numbers
 * @a: num1
 * @b: num2
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: num1
 * @b: num2
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: num1
 * @b: num2
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide a by b
 * @a: num1
 * @b: num2
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns modular of a by b
 * @a: num1
 * @b: num2
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
